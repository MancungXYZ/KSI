    private void lakukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lakukanActionPerformed
        // TODO add your handling code here:
        String text = i_decrypt.getText().toUpperCase();
        String key = koenci_decrypt.getText().toUpperCase();
        String res = "";
        
        for (int i = 0, j = 0; i < text.length(); i++)
        {
            char c = text.charAt(i);
            if (c < 'A' || c > 'Z')
                continue;
            res += (char) ((c - key.charAt(j) + 26) % 26 + 'A');
            j = ++j % key.length();
        }
        hasil_decrypt.setText(res);
    }//GEN-LAST:event_lakukanActionPerformed

