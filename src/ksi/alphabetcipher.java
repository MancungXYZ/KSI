    private void p_encryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p_encryptActionPerformed
        // TODO add your handling code here:
        String text = i_encrypt.getText().toUpperCase();
        String key = k_encrypt.getText().toUpperCase();
        String res = "";
        for (int i = 0, j = 0; i < text.length(); i++)
        {
            char c = text.charAt(i);
            if (c < 'A' || c > 'Z')
                continue;
            res += (char) ((c + key.charAt(j) - 2 * 'A') % 26 + 'A');
            j = ++j % key.length();
        }
        h_encrypt.setText(res);
    }//GEN-LAST:event_p_encryptActionPerformed

